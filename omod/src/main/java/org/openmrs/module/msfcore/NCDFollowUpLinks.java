package org.openmrs.module.msfcore;

public class NCDFollowUpLinks {

    private String visitDetailsLink;

    private String diagnosisLink;

    private String complicationsSinceLastVisitLink;

    private String prescribeMedicationLink;

    private String clinicalNoteLink;

    private String referPatientLink;

    private String requestInvestigationLink;

    private String requestAppointmentLink;

    public String getVisitDetailsLink() {
        return visitDetailsLink;
    }

    public void setVisitDetailsLink(String visitDetailsLink) {
        this.visitDetailsLink = visitDetailsLink;
    }

    public String getDiagnosisLink() {
        return diagnosisLink;
    }

    public void setDiagnosisLink(String diagnosisLink) {
        this.diagnosisLink = diagnosisLink;
    }

    public String getComplicationsSinceLastVisitLink() {
        return complicationsSinceLastVisitLink;
    }

    public void setComplicationsSinceLastVisitLink(String complicationsSinceLastVisitLink) {
        this.complicationsSinceLastVisitLink = complicationsSinceLastVisitLink;
    }

    public String getPrescribeMedicationLink() {
        return prescribeMedicationLink;
    }

    public void setPrescribeMedicationLink(String prescribeMedicationLink) {
        this.prescribeMedicationLink = prescribeMedicationLink;
    }

    public String getClinicalNoteLink() {
        return clinicalNoteLink;
    }

    public void setClinicalNoteLink(String clinicalNoteLink) {
        this.clinicalNoteLink = clinicalNoteLink;
    }

    public String getReferPatientLink() {
        return referPatientLink;
    }

    public void setReferPatientLink(String referPatientLink) {
        this.referPatientLink = referPatientLink;
    }

    public String getRequestInvestigationLink() {
        return requestInvestigationLink;
    }

    public void setRequestInvestigationLink(String requestInvestigationLink) {
        this.requestInvestigationLink = requestInvestigationLink;
    }

    public String getRequestAppointmentLink() {
        return requestAppointmentLink;
    }

    public void setRequestAppointmentLink(String requestAppointmentLink) {
        this.requestAppointmentLink = requestAppointmentLink;
    }

}
